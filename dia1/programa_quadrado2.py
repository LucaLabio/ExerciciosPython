"""O usuário informa três números
inteiros, o programa soma esses três valores e depois mostra o
quadrado do resultado obtido. """
num1 = int(input("digite um numero"))
num2 = int(input("digite um numero"))
num3 = int(input("digite um numero"))
soma = num1 + num2 + num3
resultado = soma * soma
print("O resultado da soma dos 3 valores é ", soma)
print("O quadrado da soma dos 3 valores é ", resultado)