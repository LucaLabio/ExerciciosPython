"""O usuário informa quatro valores. O
programa mostra o resultado da multiplicação do primeiro pelo
terceiro e o resultado da soma do segundo com o quarto."""
num1 = int(input("Digite um numero"))
num2 = int(input("Digite um numero"))
num3 = int(input("Digite um numero"))
num4 = int(input("Digite um numero"))
