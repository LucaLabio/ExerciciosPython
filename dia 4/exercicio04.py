"""Escreva um algoritmo que recebe um número x e y e imprime x sobre y.
 Lembre-se que ∗∗ representa a potência entre dois números em Python."""

n1 = float(input("Digite um numero"))
n2 = float(input("Digite outro numero"))
potencia = n2 ** n1
print("A potencia do primeiro numero sobre o segundo é " + str(potencia))